export function T(lang: LanguageCode, key: StringKey): string {
  return STRINGS[lang]?.[key] ?? STRINGS.pl[key] ?? key;
}

/** Substitute {placeholders} with values. */
export function substitute(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`));
}

/** Convenience: lookup + substitute in one call. */
export function Tn(lang: LanguageCode, key: StringKey, vars: Record<string, string | number>): string {
  return substitute(T(lang, key), vars);
}
